    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Tombol Add
        String starttime = jTextField2.getText().trim();
        String endtime = jTextField4.getText().trim();
        String location = jTextField3.getText().trim();
        String status = jComboBox2.getSelectedItem().toString();
        
        if (starttime.isEmpty() || endtime.isEmpty() || location.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi.");
            return;
        }

        try {
            Connection conn = DatabaseConnection.connect();

            // Ambil traineer_id dari user yang login (dari model_traineer)
            String getTraineerIdSql = "SELECT traineer_id FROM traineers WHERE user_id = ?";
            PreparedStatement stmtTraineer = conn.prepareStatement(getTraineerIdSql);
            stmtTraineer.setInt(1, traineerdata.getUserId());
            ResultSet rs = stmtTraineer.executeQuery();

            int traineerId = -1;
            if (rs.next()) {
                traineerId = rs.getInt("traineer_id");
            } else {
                JOptionPane.showMessageDialog(this, "Traineer tidak ditemukan.");
                return;
            }

            // Insert ke tabel session
            String insertSql = "INSERT INTO sessions (traineer_id, location, start_time, end_time, status) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement insertStmt = conn.prepareStatement(insertSql);
            insertStmt.setInt(1, traineerId);
            insertStmt.setString(2, location);
            insertStmt.setString(3, starttime);
            insertStmt.setString(4, endtime);
            insertStmt.setString(5, status);

            insertStmt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Session berhasil ditambahkan.");
            tampilkanDataSession(); // refresh tabel

            // Kosongkan input
            jTextField2.setText("");
            jTextField4.setText("");
            jTextField3.setText("");
            jComboBox2.setSelectedIndex(0);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Silakan pilih sesi yang ingin dihapus.");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus sesi ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) return;

        int sessionId = (int) jTable1.getValueAt(selectedRow, 0); // Kolom 0 = session_id

        try {
            Connection conn = DatabaseConnection.connect();
            String deleteSql = "DELETE FROM sessions WHERE session_id = ?";
            PreparedStatement stmt = conn.prepareStatement(deleteSql);
            stmt.setInt(1, sessionId);
            stmt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Sesi berhasil dihapus.");
            tampilkanDataSession(); // Refresh tabel
            clearForm(); // Reset field dan ID

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error saat menghapus: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

