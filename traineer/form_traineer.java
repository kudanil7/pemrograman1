    private void tampilkanDataSession() {
    try {
        Connection conn = DatabaseConnection.connect();

        // Dapatkan traineer_id dari user_id yang login
        String getTraineerIdSql = "SELECT traineer_id FROM traineers WHERE user_id = ?";
        PreparedStatement stmtTraineerId = conn.prepareStatement(getTraineerIdSql);
        stmtTraineerId.setInt(1, traineerdata.getUserId());  // model_traineer
        ResultSet rsTraineer = stmtTraineerId.executeQuery();

        int traineerId = -1;
        if (rsTraineer.next()) {
            traineerId = rsTraineer.getInt("traineer_id");
        } else {
            JOptionPane.showMessageDialog(this, "Traineer tidak ditemukan untuk user ini.");
            return;
        }

        // Ambil data sesi berdasarkan traineer_id
        String sessionSql = "SELECT s.session_id, t.name AS traineer_name, s.location, s.start_time, s.end_time, s.status " +
                            "FROM sessions s " +
                            "JOIN traineers t ON s.traineer_id = t.traineer_id " +
                            "WHERE s.traineer_id = ?";

        PreparedStatement stmtSession = conn.prepareStatement(sessionSql);
        stmtSession.setInt(1, traineerId);
        ResultSet rsSession = stmtSession.executeQuery();

        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Traineer Name");
        model.addColumn("Location");
        model.addColumn("Start Time");
        model.addColumn("End Time");
        model.addColumn("Status");

        while (rsSession.next()) {
            model.addRow(new Object[]{
                rsSession.getInt("session_id"),
                rsSession.getString("traineer_name"),
                rsSession.getString("location"),
                rsSession.getString("start_time"),
                rsSession.getString("end_time"),
                rsSession.getString("status")
            });
        }

        jTable1.setModel(model);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    
    jTable1.getSelectionModel().addListSelectionListener(e -> {
        int row = jTable1.getSelectedRow();
        if (row != -1) {
            jTextField2.setText(jTable1.getValueAt(row, 3).toString());
            jTextField4.setText(jTable1.getValueAt(row, 4).toString());
            jTextField3.setText(jTable1.getValueAt(row, 2).toString());
            jComboBox2.setSelectedItem(jTable1.getValueAt(row, 5).toString());
        }
    });
    
    
}

