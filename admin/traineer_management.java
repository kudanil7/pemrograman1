    //Proses mencari data traineer secara otomatis
    private void cariTraineer(String keyword) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // clear isi tabel sebelum isi baru

        try (Connection conn = DatabaseConnection.connect()) {
            String sql = "SELECT * FROM traineers WHERE name LIKE ? OR specialization LIKE ?";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, "%" + keyword + "%");
            stmt.setString(2, "%" + keyword + "%");
            ResultSet rs = stmt.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("traineer_id"),
                    rs.getString("name"),
                    rs.getString("specialization"),
                    rs.getString("gender")
                });
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error pencarian: " + e.getMessage());
        }
    }

