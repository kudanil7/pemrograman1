    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Add dan Update Traineer
        String name = jTextField1.getText().trim();
        String specialization = jComboBox1.getSelectedItem().toString();
        String gender = "";
        if (jRadioButton1.isSelected()) {
            gender = "Male";
        } else if (jRadioButton2.isSelected()) {
            gender = "Female";
        } else {
            JOptionPane.showMessageDialog(this, "Pilih gender terlebih dahulu!");
            return;
        }
        String experience_years = jComboBox2.getSelectedItem().toString();
        String rating = jComboBox3.getSelectedItem().toString();
        String bio = jTextArea1.getText().trim();
        
        if (name.isEmpty() || specialization.equals("Select Specialization") || experience_years.equals("Select Year") || rating.equals("Select Rating")){
            JOptionPane.showMessageDialog(this, "Semua field wajib diisi!");
            return;
        }
        
        try (Connection conn = DatabaseConnection.connect()){
            if (editId == -1){
                //Menambahkan traineer
                String insertuser = "INSERT INTO traineers (name, specialization, gender, experience_years, rating, bio) VALUES (?, ?, ?, ?, ?, ?)";
                PreparedStatement stmt = conn.prepareStatement(insertuser);
                stmt.setString(1, name);
                stmt.setString(2, specialization);
                stmt.setString(3, gender);
                stmt.setString(4, experience_years);
                stmt.setString(5, rating);
                stmt.setString(6, bio);
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data traineer berhasil disimpan.");
            }else{
                //Mengupdate traineer
                String updateQuery = "UPDATE traineers SET name=?, specialization=?, gender=?, experience_years=?, rating=?, bio=? WHERE traineer_id=?";
                PreparedStatement stmt = conn.prepareStatement(updateQuery);
                stmt.setString(1, name);
                stmt.setString(2, specialization);
                stmt.setString(3, gender);
                stmt.setString(4, experience_years);
                stmt.setString(5, rating);
                stmt.setString(6, bio);
                stmt.setInt(7, editId);
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data traineer berhasil diperbarui.");
            }
            tampilkanDataTraineer();
            clearForm();
        }catch (SQLException e){
                JOptionPane.showMessageDialog(this, "Gagal menyimpan: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Delete Traineer
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris yang ingin dihapus.");
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this,
        "Yakin ingin menghapus Traieer ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            int traineer_id = (int) jTable1.getValueAt(selectedRow, 0); // ambil ID di kolom 0
            try (Connection conn = DatabaseConnection.connect();
            PreparedStatement stmt = conn.prepareStatement("DELETE FROM traineers WHERE traineer_id = ?")) {
                stmt.setInt(1, traineer_id);
                stmt.executeUpdate();

                JOptionPane.showMessageDialog(this, "Traineer berhasil dihapus.");
                tampilkanDataTraineer(); // refresh tabel
            }catch (Exception ex){
                JOptionPane.showMessageDialog(this, "Gagal menghapus traineer: " + ex.getMessage());
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Edit Traineer
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris yang ingin diedit.");
            return;
        }
        
        int traineer_id = (int) jTable1.getValueAt(selectedRow, 0);
        try (Connection conn = DatabaseConnection.connect()){
            String hapususer = "SELECT * FROM traineers WHERE traineer_id = ?";
            PreparedStatement stmt = conn.prepareStatement(hapususer);
            stmt.setInt(1, traineer_id);
            ResultSet rs = stmt.executeQuery();
            
            if (rs.next()) {
                jTextField1.setText(rs.getString("name"));
                jComboBox1.setSelectedItem(rs.getString("specialization"));
                String gender = rs.getString("gender");
                if (gender.equalsIgnoreCase("Male")) {
                    jRadioButton1.setSelected(true);
                } else {
                    jRadioButton2.setSelected(true);
                }
                jComboBox2.setSelectedItem(rs.getString("experience_years"));
                jComboBox3.setSelectedItem(String.valueOf(rs.getInt("rating")));
                jTextArea1.setText(rs.getString("bio"));
                this.editId = traineer_id;
            }

        }   
        catch (SQLException e){
                JOptionPane.showMessageDialog(this, "Gagal Mengedit: " + e.getMessage());
        }     
    }//GEN-LAST:event_jButton2ActionPerformed

