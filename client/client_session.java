    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Delete Booking
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris booking yang ingin dihapus.");
            return;
        }

        // Dapatkan detail booking_time atau ID booking (gunakan yang ditampilkan)
        String bookingTime = jTable1.getValueAt(selectedRow, 4).toString(); // asumsi kolom ke-4 adalah booking_time

        int confirm = JOptionPane.showConfirmDialog(this,
                "Yakin ingin menghapus booking ini?", "Konfirmasi",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try (Connection conn = DatabaseConnection.connect()) {
                String sql = "DELETE FROM bookings WHERE user_id = ? AND booking_time = ?";
                PreparedStatement stmt = conn.prepareStatement(sql);
                stmt.setInt(1, client.getUserId());
                stmt.setString(2, bookingTime);
                int affectedRows = stmt.executeUpdate();

                if (affectedRows > 0) {
                    JOptionPane.showMessageDialog(this, "Booking berhasil dihapus.");
                    tampilkanDataBooking(); // refresh tabel
                } else {
                    JOptionPane.showMessageDialog(this, "Booking tidak ditemukan.");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal menghapus booking: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

