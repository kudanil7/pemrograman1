    private void tampilkanDataBooking() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Traineer Name");
        model.addColumn("Location");
        model.addColumn("Start Time");
        model.addColumn("End Time");
        model.addColumn("Booking Time");
        model.addColumn("Status");

        try (Connection conn = DatabaseConnection.connect()) {
            String sql = "SELECT t.name AS traineer_name, s.location, s.start_time, s.end_time, b.booking_time, b.status " +
                         "FROM bookings b " +
                         "JOIN sessions s ON b.session_id = s.session_id " +
                         "JOIN traineers t ON s.traineer_id = t.traineer_id " +
                         "WHERE b.user_id = ?";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setInt(1, client.getUserId());  // Ambil user_id dari client yang login
            ResultSet rs = stmt.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("traineer_name"),
                    rs.getString("location"),
                    rs.getString("start_time"),
                    rs.getString("end_time"),
                    rs.getString("booking_time"),
                    rs.getString("status")
                });
            }

            jTable1.setModel(model); // Ganti dengan nama JTable kamu
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }
